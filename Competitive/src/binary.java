import java.util.Scanner;

public class binary {
	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		
		while (scan.hasNext()) {
			String bin1 = scan.next();
			String bin2 = scan.next();
			System.out.println(bin1);
			System.out.println(bin2);
			
			
		}
		scan.close();
	}
}
